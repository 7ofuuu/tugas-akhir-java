    public void datatable(){
        String[] kolom = {"ID Barang","Nama Barang","Penyimpanan","Kondisi Barang","Stok Barang"};
        
        DefaultTableModel tbl = new DefaultTableModel(null, kolom);
        try {
            Statement stmt = (Statement) library.GetConnection().createStatement();
            String query = "SELECT * FROM barang";
            ResultSet rs = stmt.executeQuery(query);
            while (rs.next()) {
                String id = rs.getString("id");
                String nama = rs.getString("nama");
                String penyimpanan = rs.getString("penyimpanan");
                String kondisi = rs.getString("kondisi");
                String stok = rs.getString("stok");
                        
                tbl.addRow(new String[]{id, nama, penyimpanan, kondisi, stok});
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        table.setModel(tbl);
    }


    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        String id= txtid.getText();
        String nama = txtnama.getText();
        String penyimpanan = (String) cmbpny.getSelectedItem();
        String stok = txtstok.getText();
        String kondisi = null;

        if (jRadioButton1.isSelected()){
            kondisi="Baru";
        }else if (jRadioButton2.isSelected()){
            kondisi="Bekas";
        }

        try {
            Statement statement = (Statement) library.GetConnection().createStatement();
            statement.executeUpdate("INSERT INTO barang VALUES ('" + id + "','" + nama + "','"+penyimpanan+"','"+kondisi+"','"+stok+"');");
            statement.close();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
        }catch (Exception t){
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan");
        }

        datatable();
    }//GEN-LAST:event_btnsimpanActionPerformed

    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        String id = txtid.getText();

        try {
            Statement statement = (Statement) library.GetConnection().createStatement();
            statement.executeUpdate("DELETE FROM barang WHERE id=('"+ id +"');");
            statement.close();
            JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
            txtid.setText("");
            txtnama.setText("");
            cmbpny.setSelectedItem("");
            String kondisi = null;
            txtstok.setText("");
            txtid.requestFocus();
        } catch (Exception t) {
            JOptionPane.showMessageDialog(null, "Data Gagal Dihapus");
        }
        DataBarang main = new DataBarang();

        main.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnhapusActionPerformed

    private void btncariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncariActionPerformed
        try{
            Statement statement = (Statement) library.GetConnection().createStatement();
            ResultSet res = statement.executeQuery("SELECT * FROM barang WHERE" +  "id='" + txtid.getText() + "nama='" + txtnama.getText() + "'");
            DefaultTableModel tbl =  new DefaultTableModel();
            tbl.addColumn("ID Barang");
            tbl.addColumn("Nama Barang");
            tbl.addColumn("Penyimpanan");

            table.setModel(tbl);

            while (res.next()) {
                tbl.addRow(new Object[]{
                    res.getString("id"),
                    res.getString("nama"),
                    res.getString("penyimpanan")
                });

                table.setModel(tbl);
            }

        }catch(Exception e) {
            JOptionPane.showMessageDialog(rootPane, "SALAH");
        }
    }//GEN-LAST:event_btncariActionPerformed

